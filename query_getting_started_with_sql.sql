PRAGMA table_info(PRODUCT); --VERIFICANDO INFORMÇÕES DE TABELAS(NOME E TIPOS DE DADOS);
SELECT * FROM PRODUCT; -- RECUPERANDO INFORMAÇÕES DA TABELA PRODUCT;
SELECT PRODUCT_ID, DESCRIPTION, PRICE, round(PRICE * 1.25, 2) AS TAXED_PRICE_25 FROM PRODUCT; -- CONSULTA NA TABELA PRODUCT E CRIAÇÃO DE COLUNA COM PREÇO ADICIONANDO 25% AO VALOR ATUAL TAMBÉM UTILIZAMOS A FUNÇÃO ROUND PARA DEFINIR AS CASAS DECIMAIS;
SELECT PRODUCT_ID, DESCRIPTION, PRICE AS UNTAXED_PRICE, round(PRICE * 1.25, 2) AS TAXED_PRICE_25 FROM PRODUCT; -- RENOMEANDO A COLUNA PRICE PARA UNTAXED_PRICE;
