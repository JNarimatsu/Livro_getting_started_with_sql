SELECT NAME, CITY || ', ' || STATE AS LOCATION FROM CUSTOMER; --CONCAQUETANAÇÃO DE TEXTO
SELECT CUSTOMER_ID, NAME, CITY, STATE FROM CUSTOMER WHERE STATE = 'TX'; --VERIFICANDO CLIENTES DO ESTADO DE TEXAS
SELECT CUSTOMER_ID, NAME, CITY, STATE FROM CUSTOMER WHERE STATE != 'TX'; --VERIFICANDO CLIENTES QUE NÃO SÃO DO ESTADO DE TEXAS


SELECT NAME, SUM(ORDER_QTY * PRICE) AS REVENUE FROM CUSTOMER INNER JOIN CUSTOMER_ORDER ON CUSTOMER.CUSTOMER_ID = CUSTOMER_ORDER.CUSTOMER_ID INNER JOIN PRODUCT ON PRODUCT.PRODUCT_ID = CUSTOMER_ORDER.PRODUCT_ID GROUP BY NAME -- PUXANDO INFORMAÇÕES DE TOTAIS DE VENDAS E AGRUPANDO POR CLIENTES.

